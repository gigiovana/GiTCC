        <li>Antes de cada questão, o jogo mostrará um lembrete do conteúdo a ser abordado;</li>
        <li>A próxima tela após o lembrete é a que apresenta a questão em si;</li>
        <li>Cada questão conterá somente uma resposta correta;</li>
        <li>As telas do jogo são compostas por um menu na parte superior com as seguintes opções: Página inicial, cadastro, login, descrição do jogo, como jogar e sobre nós;</li>
        <li>Algumas telas possuem uma tradução do português para a Libras, por meio de um vídeo realizado por uma intérprete;</li>
        <li>O jogo é composto na sua primeira fase, por dez questões de modelos distintos, sendo eles: puxe e arraste, ligue as colunas, forme a frase, múltipla escolha e discursiva;</li>
