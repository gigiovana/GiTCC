    <h1> Como Jogar:</h1>
    <br>
    <ol class="listalb">
        <li>É necessário o usuário realizar o seu cadastro antes de começar a jogar;</li>
        <li>Cada pessoa possuirá um login e senha distintos;</li>
        <li>Quando o usuário entrar no jogo pela segunda vez, não será necessário a realização do cadastro, pois ele usará o login e senha já cadastrados e retornará no ponto em que parou;</li>
        <li>Na tela inicial do jogo, há um botão denominado “COMO JOGAR”, caso o usuário clique neste botão ele será direcionado a uma tela com instruções sobre as regras do jogo; </li>
